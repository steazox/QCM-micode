function showQuestion(index) {
    const questionData = questions[index];
    questionContainer.innerHTML = `
        <p>${questionData.question}</p>
        ${questionData.answers.map(answer => `
            <label>
                <input type="radio" name="q${index}" value="${answer.correct}">
                ${answer.text}
            </label>
        `).join("")}
    `;

    updateProgressBar();
}

let answerClicked = false;

questionContainer.addEventListener("click", (event) => {
    const clickedInput = event.target.closest("input[type='radio']");
    if (clickedInput && !answerClicked) {
        answerClicked = true;
        
        const questionIndex = parseInt(clickedInput.name.substring(1));
        const selectedAnswer = clickedInput.value === "true";

        if (selectedAnswer) {
            clickedInput.parentElement.classList.add("correct");
        } else {
            clickedInput.parentElement.classList.add("incorrect");
        }

        setTimeout(() => {
            answerClicked = false;
            
            if (selectedAnswer || !selectedAnswer) {
                const currentLabels = questionContainer.querySelectorAll("label");
                currentLabels.forEach(label => {
                    label.classList.remove("correct", "incorrect");
                });
                
                if (questionIndex === currentQuestionIndex) {
                    if (currentQuestionIndex < questions.length - 1) {
                        currentQuestionIndex++;
                        showQuestion(currentQuestionIndex);
                    } else {
                        showResults();
                    }
                }
            }
        }, 1000); // Délai d'une seconde
    }
});

